n =input('digite alguma coisa ')
print (n.isnumeric())#para identificar se é um numero
print (n.isalpha())#para identificar se é texto
print (n.isalnum())#identificar se é texto ou numero
print (n.isupper())#identifica s tem letra maiuscula
print (n.isdecimal())#identifica se tem numero decimal
print (n.islower())#para verificar se tem letras minusculas
print (n.isspace())#verificar se tem espaço
